public class Program8 {
    public static void main(String[] args) {
        int x = 10;
        System.out.println("the no is = "+ x);
        x++;
        System.out.println("the no is = "+x);
        ++x;
        System.out.println("the no is = "+x);
        x--;
        System.out.println("the no is = "+x);
        --x;
        System.out.println("the no is = "+x);
        
    }
    
}
